package com.example.it.customlistview;

public class PersonalData {
    private String member_id;
    private String member_image;
    private String member_top;
    private String member_bottom;

    public String getMember_id() {
        return member_id;
    }
    public void setMember_id(String member_id) {
        this.member_id = member_id;
    }

    public String getMember_top() {
        return member_top;
    }
    public void setMember_top(String member_top) {
        this.member_top = member_top;
    }

    public String getMember_bottom() {
        return member_bottom;
    }
    public void setMember_bottom(String member_bottom) {
        this.member_bottom = member_bottom;
    }

    public void setMember_image(String member_image) {
        this.member_image = member_image;
    }
    public String getMember_image() {
        return member_image;
    }


}